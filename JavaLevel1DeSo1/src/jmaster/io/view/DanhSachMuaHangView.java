    private void initScreen() {
        dsmhTable.setModel(tableModel);
        //set ten cot
        tableModel.addColumn("Ma kh");
        tableModel.addColumn("Ho ten");
        tableModel.addColumn("kieu xe");
        tableModel.addColumn("Gia thue");
        tableModel.addColumn("So luong xe");

        //load data khach hang, mat hang cho combo box
        loadKHData();
        loadMHData();
        
        //load du lieu mac dinh 
        sapXepTheoTenKH();
    }

    private void loadKHData() {
        try {
            ArrayList<KhachHang> khachHangList = khachHangService.findAll();

            for (KhachHang khachHang : khachHangList) {
                khComboBox.addItem(khachHang);
            }
        } catch (Exception ex) {
            Logger.getLogger(DanhSachKhachHangView.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Loi doc du lieu", "Loi", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void loadMHData() {
        try {
            ArrayList<Oto> matHangList = matHangService.findAll();

            for (Oto matHang : matHangList) {
                mhComboBox.addItem(matHang);
            }
        } catch (Exception ex) {
            Logger.getLogger(DanhSachKhachHangView.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Loi doc du lieu", "Loi", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void sapXepTheoTenKH() {
        try {
            tableModel.setRowCount(0);//xoa du lieu cu

            ArrayList<jmaster.io.model.DanhSachMuaHang> danhSachMuaHangs = danhSachMuaHangService.sortByTenKH();

            for (jmaster.io.model.DanhSachMuaHang dsmh : danhSachMuaHangs) {
                tableModel.addRow(new Object[]{
                    dsmh.getKhachHang().getMaKH(),
                    dsmh.getKhachHang().getHoTen(),
                    dsmh.getOto().getKieuXe(),
                    dsmh.getOto().getSoTienThue(),
                    dsmh.getSoLuong()
                });
            }
        } catch (Exception ex) {
            Logger.getLogger(DanhSachMuaHangView.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Loi doc du lieu", "Loi", JOptionPane.ERROR_MESSAGE);
        }
    }

