    private void themDSMH() {
        DanhSachMuaHang dsmh = new DanhSachMuaHang();

        KhachHang khachHang = (KhachHang) khComboBox.getSelectedItem();
        dsmh.setKhachHang(khachHang);

        Oto matHang = (Oto) mhComboBox.getSelectedItem();
        dsmh.setOto(matHang);

        String soLuongMua = soLuongMuaTxt.getText();
        if (Validator.isValidInteger(soLuongMua)) {
            dsmh.setSoLuong(Integer.parseInt(soLuongMua));
        } else {
            JOptionPane.showMessageDialog(this, "Vui long nhap lai", "Loi", JOptionPane.ERROR_MESSAGE);
            return;//ko cho chay tiep
        }

        try {
            //kiem tra khach hang da mua mat hang chua
            if (Validator.validateDSMH(dsmh)) {
                danhSachMuaHangService.create(dsmh);

                //reload lai data
                sapXepTheoTenKH();
            } else {
                JOptionPane.showMessageDialog(this, "Mat hang da chon thue roi", "Loi", JOptionPane.ERROR_MESSAGE);
                return;//ko cho chay tiep
            }
        } catch (Exception ex) {
            Logger.getLogger(DanhSachMuaHangView.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Loi doc du lieu", "Loi", JOptionPane.ERROR_MESSAGE);
        }
    }
